// =============================================================================
// config.h - ピン定義・定数設定
// =============================================================================
// このファイルのみ変更すればハードウェア構成の違いに対応できる
// =============================================================================

#ifndef CONFIG_H
#define CONFIG_H

// -----------------------------------------------------------------------------
// シリアル通信設定
// -----------------------------------------------------------------------------
#define SERIAL_BAUD_RATE      9600

// コマンド受信バッファサイズ (bytes)
#define CMD_BUFFER_SIZE       64

// コマンド区切り文字
#define CMD_TERMINATOR        '\n'

// -----------------------------------------------------------------------------
// ピン定義 - 土壌湿度センサー (アナログ入力)
// -----------------------------------------------------------------------------
#define SOIL_SENSOR_COUNT     2
#define PIN_SOIL_1            A0
#define PIN_SOIL_2            A1

// -----------------------------------------------------------------------------
// ピン定義 - 水位センサー (フロートスイッチ, デジタル入力)
// -----------------------------------------------------------------------------
#define PIN_WATER_LEVEL       2    // HIGH = 水あり, LOW = 水不足

// -----------------------------------------------------------------------------
// ピン定義 - ポンプ制御用ラッチリレー (デジタル出力)
// -----------------------------------------------------------------------------
#define PIN_PUMP_RELAY        7

// リレーON/OFFの論理 (リレーモジュールによって反転する場合がある)
#define RELAY_ON              HIGH
#define RELAY_OFF             LOW

// -----------------------------------------------------------------------------
// ピン定義 - 照度センサー (LDR + 10kΩ 分圧回路, アナログ入力)
// -----------------------------------------------------------------------------
#define PIN_LIGHT             A2   // LDR アナログ入力

// LDR → lux 変換定数 (10kΩ 分圧回路の場合の目安)
// lux = LIGHT_LUX_MULT / (1023 - analogValue) - LIGHT_LUX_OFFSET
// 要キャリブレーション: 実環境に合わせて調整してください
#define LIGHT_REF_RESISTANCE  10000.0  // 分圧抵抗 (Ω)
#define LIGHT_LUX_MULT        500000.0 // LDR 特性による乗数
#define LIGHT_LUX_OFFSET      0.0

// -----------------------------------------------------------------------------
// ピン定義 - EC/TDS センサー (土壌電気伝導度, アナログ入力)
// -----------------------------------------------------------------------------
// TDS Meter v1.0 モジュール (DFRobot SEN0244 等) を想定
// EC (mS/cm) = TDS (ppm) * 2 / 1000  (目安)
#define PIN_EC                A3   // TDS/EC アナログ入力
#define EC_VREF               5.0  // Arduino 基準電圧 (V)
#define EC_TEMP_COEFF         0.02 // 温度補償係数 (2%/°C)
#define EC_KVALUE             1.0  // セル定数 (キャリブレーションで調整)

// -----------------------------------------------------------------------------
// ピン定義 - DHT22 温湿度センサー (デジタル入力)
// -----------------------------------------------------------------------------
#define PIN_DHT               4
#define DHT_TYPE              DHT22

// -----------------------------------------------------------------------------
// 安全機能 - タイムアウト設定
// -----------------------------------------------------------------------------

// ポンプ自動OFF: PUMP_ON後この時間(ms)でポンプを自動停止
// ラズパイがフリーズしても水浸しを防ぐ
#define PUMP_TIMEOUT_MS       60000UL   // 60秒

// ウォッチドッグ: シリアル通信が途絶えた場合にポンプを停止する時間(ms)
// 0 = ウォッチドッグ無効
#define WATCHDOG_TIMEOUT_MS   300000UL  // 5分

// -----------------------------------------------------------------------------
// センサー読み取り設定
// -----------------------------------------------------------------------------

// アナログセンサーの読み取り回数 (中央値フィルタ用)
#define ANALOG_READ_SAMPLES   5

// 各サンプル間の待機時間 (ms)
#define ANALOG_READ_DELAY_MS  10

// -----------------------------------------------------------------------------
// ファームウェア情報
// -----------------------------------------------------------------------------
#define FW_VERSION            "1.2.0"
#define FW_NAME               "WateringDriver"

#endif // CONFIG_H
